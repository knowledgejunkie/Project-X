	private static void getFrameLevel(double[][][][] _sample)
	{
		double[] sample_level = new double[2];

		for (int gr = 0; gr < 12; gr++)
			for (int sb = 0; sb < 32; sb++)
				for (int i = 0; i < 3; i++)
					for(int ch = 0; ch < 2; ch++)
						if (Math.abs(_sample[gr][i][sb][ch]) > sample_level[ch])
							sample_level[ch] = Math.abs(_sample[gr][i][sb][ch]);

		out1.reset();
		out1.write((byte) (sample_level[0] * 100.0));
		out1.write((byte) (sample_level[1] * 100.0));
	}

