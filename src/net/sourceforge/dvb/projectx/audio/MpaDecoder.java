	public static void loadbits(int size)
	{
		Bits = new boolean[size*8];
		BitPos = 0;

		for (int a=0; a < size; a++)
			for (int b=0; b < 8; b++)
				if (((0x80>>>b) & buf[BufferPos + a]) != 0)
					Bits[(a * 8) + b] = true;

		BufferPos += size;
	}

